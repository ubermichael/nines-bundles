namespace Nines\BlogBundle\Tests\Repository;

use Nines\BlogBundle\Repository\PostRepository;
use Nines\UtilBundle\TestCase\ServiceTestCase;

class PostRepositoryTest extends ServiceTestCase {
    private ?PostRepository $repo = null;

    public function testSetUp() : void {
        $this->assertInstanceOf(PostRepository::class, $this->repo);
    }

    public function testIndexQuery() : void {
        $this->assertCount(2, $this->repo->indexQuery()->execute());
    }

    public function testIndexQueryIncludePrivate() : void {
        $this->assertCount(5, $this->repo->indexQuery(true)->execute());
    }

    public function testIndexQueryLimit() : void {
        $this->assertCount(1, $this->repo->indexQuery(false, 1)->execute());
    }

    public function testSearchQuery() : void {
        $this->assertCount(2, $this->repo->searchQuery('paragraph')->execute());
    }

    public function testSearchQueryIncludePrivate() : void {
        $this->assertCount(5, $this->repo->searchQuery('paragraph', true)->execute());
    }

    protected function setUp() : void {
        parent::setUp(); // TODO: Change the autogenerated stub
        $this->repo = self::$container->get(PostRepository::class);
    }
}
